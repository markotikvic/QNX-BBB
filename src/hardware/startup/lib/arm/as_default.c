#include <startup.h>

unsigned
as_default()
{
	unsigned ret;

	// Allow full range of address space based on size of paddr_bits
	// LPAE address are still limited to 40 bits
	ret = as_add(0, ((1ULL << paddr_bits)-1), AS_ATTR_NONE, "memory", AS_NULL_OFF);

	return ret;
}

